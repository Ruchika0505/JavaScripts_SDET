// Variables in JavaScript

// var example
var city = "Chandigarh";
console.log(city);

city = "Delhi";
console.log(city);

// let example
let age = 25;
console.log(age);

age = 26;
console.log(age);

// const example
const country = "India";
console.log(country);

// const cannot be reassigned
